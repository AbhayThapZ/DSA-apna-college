#include <iostream>
using namespace std;
int main(){
    int a=6,b=10;
    cout<<(a&b)<<endl;//Bitwise Operator AND
    cout<<(a|b)<<endl;//Bitwise Operator OR
    cout<<(a^b)<<endl;//Bitwise Operator XOR
    cout<<(10<<2)<<endl;//Bitwise Operator LEFT SHIFT
    cout<<(10>>1)<<endl;//Bitwise Operator RIGHT SHIFT
    return 0;
}